// ── D-Bus introspection XML for org.kde.StatusNotifierItem ────────────────
// The SNI object path.  GNOME AppIndicator expects paths under
// /org/ayatana/NotificationItem/; KDE uses /StatusNotifierItem.
// We use the Ayatana convention for broader compatibility.
static constexpr const char s_sni_object_path[] = "/StatusNotifierItem";
static constexpr const char s_sni_menu_path[]   = "/StatusNotifierItem/Menu";

static constexpr const char s_sni_introspection[] =
    "<node>"
    "  <interface name='org.kde.StatusNotifierItem'>"
    "    <property name='Category' type='s' access='read'/>"
    "    <property name='Id' type='s' access='read'/>"
    "    <property name='Title' type='s' access='read'/>"
    "    <property name='Status' type='s' access='read'/>"
    "    <property name='WindowId' type='i' access='read'/>"
    "    <property name='IconThemePath' type='s' access='read'/>"
    "    <property name='IconName' type='s' access='read'/>"
    "    <property name='IconPixmap' type='a(iiay)' access='read'/>"
    "    <property name='Menu' type='o' access='read'/>"
    "    <method name='ContextMenu'>"
    "      <arg type='i' name='x' direction='in'/>"
    "      <arg type='i' name='y' direction='in'/>"
    "    </method>"
    "    <method name='Activate'>"
    "      <arg type='i' name='x' direction='in'/>"
    "      <arg type='i' name='y' direction='in'/>"
    "    </method>"
    "    <method name='SecondaryActivate'>"
    "      <arg type='i' name='x' direction='in'/>"
    "      <arg type='i' name='y' direction='in'/>"
    "    </method>"
    "    <method name='Scroll'>"
    "      <arg type='i' name='delta' direction='in'/>"
    "      <arg type='s' name='orientation' direction='in'/>"
    "    </method>"
    "    <signal name='NewTitle'/>"
    "    <signal name='NewIcon'/>"
    "    <signal name='NewStatus'/>"
    "  </interface>"
    "</node>";

// ── implementation ─────────────────────────────────────────────────────────
struct SystemTray::Impl
{
    saucer::application *app{};
    saucer::window &window;

    GDBusConnection *conn{};
    guint sni_reg_id{};
    guint watcher_signal_id{};

    DbusmenuServer *menu_server{};
    DbusmenuMenuitem *root_menu{};
    DbusmenuMenuitem *show_hide_item{};

    std::string icon_bw_path;
    std::string icon_active_path;
    bool visible{true};
    bool active{}; // true → accent icon, false → B&W icon

    GMainLoop *loop{};  // runs on a helper thread

    // D-Bus needs a running GMainLoop. Saucer uses ereignis event loop,
    // not GLib's, so we spin our own in a helper thread.
    std::thread thread;

    Impl(saucer::window &win) : window(win) {}
    ~Impl()
    {
        if (loop)
            g_main_loop_quit(loop);
        if (thread.joinable())
            thread.join();
        if (sni_reg_id)
            g_dbus_connection_unregister_object(conn, sni_reg_id);
        if (watcher_signal_id)
            g_dbus_connection_signal_unsubscribe(conn, watcher_signal_id);
        if (menu_server)
            g_object_unref(menu_server);
        if (conn)
            g_object_unref(conn);
    }
};

// ── SNI property getter ────────────────────────────────────────────────────
static GVariant *
get_sni_property(GDBusConnection *conn, const gchar *sender,
                 const gchar *object_path, const gchar *interface_name,
                 const gchar *property_name, GError **error, gpointer user_data)
{
    auto *impl = static_cast<SystemTray::Impl *>(user_data);

    if (g_strcmp0(property_name, "Category") == 0)
        return g_variant_new_string("ApplicationStatus");
    if (g_strcmp0(property_name, "Id") == 0)
        return g_variant_new_string("treeble");
    if (g_strcmp0(property_name, "Title") == 0)
        return g_variant_new_string("Treeble");
    if (g_strcmp0(property_name, "Status") == 0)
        return g_variant_new_string("Active");
    if (g_strcmp0(property_name, "WindowId") == 0)
        return g_variant_new_int32(0);
    if (g_strcmp0(property_name, "IconThemePath") == 0)
        return g_variant_new_string("");
    if (g_strcmp0(property_name, "IconName") == 0)
    {
        // GNOME AppIndicator expects an absolute file path, not a theme name
        auto &path = impl->active ? impl->icon_active_path : impl->icon_bw_path;
        return g_variant_new_string(path.c_str());
    }
    if (g_strcmp0(property_name, "IconPixmap") == 0)
    {
        auto *data = impl->active ? s_pixmap_active : s_pixmap_bw;
        auto  size = impl->active ? s_pixmap_active_size : s_pixmap_bw_size;

        // Build a(iiay) — array of (width, height, pixel_data)
        auto *bytes = g_variant_new_fixed_array(G_VARIANT_TYPE_BYTE,
                                                  data, size, 1);
        auto *entry = g_variant_new("(ii@ay)", 22, 22, bytes);

        auto *builder = g_variant_builder_new(G_VARIANT_TYPE("a(iiay)"));
        g_variant_builder_add_value(builder, entry);
        // entry is now owned by builder — do NOT unref it
        auto *result = g_variant_builder_end(builder);
        g_variant_builder_unref(builder);
        return result;
    }

    if (g_strcmp0(property_name, "Menu") == 0)
        return g_variant_new_object_path(s_sni_menu_path);

    return nullptr;
}

// ── SNI method calls ───────────────────────────────────────────────────────
static void
handle_sni_method_call(GDBusConnection *conn, const gchar *sender,
                       const gchar *object_path, const gchar *interface_name,
                       const gchar *method_name, GVariant *parameters,
                       GDBusMethodInvocation *invocation, gpointer user_data)
{
    auto *impl = static_cast<SystemTray::Impl *>(user_data);

    if (g_strcmp0(method_name, "Activate") == 0 || g_strcmp0(method_name, "SecondaryActivate") == 0)
    {
        // Toggle window visibility
        if (impl->visible)
        {
            impl->window.hide();
            impl->visible = false;
            dbusmenu_menuitem_property_set(impl->show_hide_item,
                                            DBUSMENU_MENUITEM_PROP_LABEL, "Show");
        }
        else
        {
            impl->window.show();
            impl->visible = true;
            dbusmenu_menuitem_property_set(impl->show_hide_item,
                                            DBUSMENU_MENUITEM_PROP_LABEL, "Hide");
        }
        g_dbus_method_invocation_return_value(invocation, nullptr);
    }
    else if (g_strcmp0(method_name, "ContextMenu") == 0)
    {
        // The panel is requesting the context menu — nothing to do here,
        // dbusmenu-server handles it.
        g_dbus_method_invocation_return_value(invocation, nullptr);
    }
    else if (g_strcmp0(method_name, "Scroll") == 0)
    {
        g_dbus_method_invocation_return_value(invocation, nullptr);
    }
    else
    {
        g_dbus_method_invocation_return_error(invocation, G_DBUS_ERROR,
                                               G_DBUS_ERROR_UNKNOWN_METHOD,
                                               "Unknown method: %s", method_name);
    }
}

// ── SNI interface vtable ────────────────────────────────────────────────────
static const GDBusInterfaceVTable s_sni_vtable = {
    handle_sni_method_call,
    get_sni_property,
    nullptr // set_property – not needed, all properties are read-only
};

struct IconPaths { std::string bw; std::string active; };

static IconPaths write_icons()
{
    const char *data_dir = g_get_user_data_dir();
    auto base = std::string(data_dir) + "/treeble/tray-icons";
    g_mkdir_with_parents(base.c_str(), 0755);

    auto icon_bw = base + "/treeble.png";
    auto icon_ac = base + "/treeble-active.png";

    if (!g_file_test(icon_bw.c_str(), G_FILE_TEST_EXISTS))
        g_file_set_contents(icon_bw.c_str(),
                             reinterpret_cast<const char *>(s_tray_icon_bw),
                             s_tray_icon_bw_size, nullptr);
    if (!g_file_test(icon_ac.c_str(), G_FILE_TEST_EXISTS))
        g_file_set_contents(icon_ac.c_str(),
                             reinterpret_cast<const char *>(s_tray_icon_active),
                             s_tray_icon_active_size, nullptr);

    return {icon_bw, icon_ac};
}

// ── constructor ─────────────────────────────────────────────────────────────
SystemTray::SystemTray(saucer::application *app, saucer::window &window)
    : m_impl(std::make_unique<Impl>(window))
{
    m_impl->app = app;

    // ── write icons ──────────────────────────────────────────────────────
    {
        auto paths = write_icons();
        m_impl->icon_bw_path = paths.bw;
        m_impl->icon_active_path = paths.active;
    }

    // ── get session bus on a dedicated GMainContext ──────────────────────
    // GNOME AppIndicator needs D-Bus method calls to be answered; the
    // dedicated context + thread ensures GDBus processes them.
    GError *dbus_err = nullptr;

    // Connect to session bus
    m_impl->conn = g_bus_get_sync(G_BUS_TYPE_SESSION, nullptr, &dbus_err);
    if (!m_impl->conn)
    {
        std::fprintf(stderr, "[tray] failed to connect to D-Bus: %s\n", dbus_err->message);
        g_error_free(dbus_err);
        return;
    }

    // Start a GMainLoop thread so D-Bus method calls are processed
    m_impl->loop = g_main_loop_new(nullptr, FALSE);
    m_impl->thread = std::thread([loop = m_impl->loop]()
    {
        g_main_loop_run(loop);
    });

    // ── create menu ──────────────────────────────────────────────────────
    m_impl->root_menu = dbusmenu_menuitem_new();

    auto *show_hide = dbusmenu_menuitem_new();
    dbusmenu_menuitem_property_set(show_hide, DBUSMENU_MENUITEM_PROP_LABEL, "Hide");
    dbusmenu_menuitem_property_set_bool(show_hide, DBUSMENU_MENUITEM_PROP_ENABLED, TRUE);
    m_impl->show_hide_item = show_hide;

    g_signal_connect_swapped(show_hide, DBUSMENU_MENUITEM_SIGNAL_ITEM_ACTIVATED,
                              G_CALLBACK(+[](SystemTray::Impl *impl) {
                                  if (impl->visible)
                                  {
                                      impl->window.hide();
                                      impl->visible = false;
                                      dbusmenu_menuitem_property_set(
                                          impl->show_hide_item,
                                          DBUSMENU_MENUITEM_PROP_LABEL, "Show");
                                  }
                                  else
                                  {
                                      impl->window.show();
                                      impl->visible = true;
                                      dbusmenu_menuitem_property_set(
                                          impl->show_hide_item,
                                          DBUSMENU_MENUITEM_PROP_LABEL, "Hide");
                                  }
                              }),
                              m_impl.get());

    auto *quit = dbusmenu_menuitem_new();
    dbusmenu_menuitem_property_set(quit, DBUSMENU_MENUITEM_PROP_LABEL, "Quit");
    dbusmenu_menuitem_property_set_bool(quit, DBUSMENU_MENUITEM_PROP_ENABLED, TRUE);
    g_signal_connect_swapped(quit, DBUSMENU_MENUITEM_SIGNAL_ITEM_ACTIVATED,
                              G_CALLBACK(+[](saucer::application *a) {
                                  a->quit();
                              }),
                              app);

    dbusmenu_menuitem_child_append(m_impl->root_menu, show_hide);
    dbusmenu_menuitem_child_append(m_impl->root_menu, quit);

    // ── create dbusmenu server ───────────────────────────────────────────
    m_impl->menu_server = dbusmenu_server_new(s_sni_menu_path);
    dbusmenu_server_set_root(m_impl->menu_server, m_impl->root_menu);

    // ── register StatusNotifierItem on the bus ───────────────────────────
    GDBusNodeInfo *node = g_dbus_node_info_new_for_xml(s_sni_introspection, nullptr);

    auto id = g_dbus_connection_register_object(
        m_impl->conn, s_sni_object_path,
        node->interfaces[0], // the first (and only) interface
        &s_sni_vtable, m_impl.get(), nullptr, &dbus_err);

    g_dbus_node_info_unref(node);

    if (id == 0)
    {
        std::fprintf(stderr, "[tray] failed to register SNI object: %s\n", dbus_err->message);
        g_error_free(dbus_err);
        return;
    }
    m_impl->sni_reg_id = id;

    // ── register with watcher ────────────────────────────────────────────
    auto *proxy = g_dbus_proxy_new_sync(m_impl->conn, G_DBUS_PROXY_FLAGS_NONE, nullptr,
                                         "org.kde.StatusNotifierWatcher",
                                         "/StatusNotifierWatcher",
                                         "org.kde.StatusNotifierWatcher",
                                         nullptr, &dbus_err);
    // Keep the context current for the remaining D-Bus calls
    // (we'll pop at the end of this block)
    if (!proxy)
    {
        std::fprintf(stderr, "[tray] StatusNotifierWatcher not available: %s\n", dbus_err->message);
        g_error_free(dbus_err);
        // Not fatal — the icon just won't appear until a watcher connects
        return;
    }

    // Register with busname@objectpath so the watcher knows our object path
    auto *own_name = g_dbus_connection_get_unique_name(m_impl->conn);
    auto *result = g_dbus_proxy_call_sync(proxy, "RegisterStatusNotifierItem",
                                            g_variant_new("(s)", own_name),
                                            G_DBUS_CALL_FLAGS_NONE, -1, nullptr, &dbus_err);
    if (!result)
    {
        std::fprintf(stderr, "[tray] failed to register with watcher: %s\n", dbus_err->message);
        g_error_free(dbus_err);
    }
    else
    {
        g_variant_unref(result);
    }

    g_object_unref(proxy);

    // ── track window visibility ──────────────────────────────────────────
    // ponytail: minimize button simply minimizes the window to the taskbar
    // (standard GTK behaviour), close button hides to tray — no duplication.
    window.on<saucer::window::event::close>([this]() -> saucer::policy
    {
        m_impl->window.hide();
        m_impl->visible = false;
        dbusmenu_menuitem_property_set(m_impl->show_hide_item,
                                        DBUSMENU_MENUITEM_PROP_LABEL, "Show");
        return saucer::policy::block;
    });

    std::fprintf(stderr, "[tray] registered StatusNotifierItem\n");
}

// ── public API ─────────────────────────────────────────────────────────────
void SystemTray::set_active(bool active)
{
    if (!m_impl->conn || m_impl->active == active)
        return;

    m_impl->active = active;

    // Tell the panel to re-read the IconName property
    GError *dbus_err = nullptr;
    g_dbus_connection_emit_signal(
        m_impl->conn,
        nullptr, // broadcast
        s_sni_object_path,
        "org.kde.StatusNotifierItem",
        "NewIcon",
        nullptr, // no parameters
        &dbus_err);

    if (dbus_err)
    {
        std::fprintf(stderr, "[tray] failed to emit NewIcon: %s\n", dbus_err->message);
        g_error_free(dbus_err);
    }
}

